(function initMouseParticles() {
  let lastParticleTime = 0;
  
  // 粒子图库：为四个季节主题配置不同的表情符号
  const themeParticles = {
    0: ['🌸', '💮'],       // 春：樱花、花瓣
    1: ['🍃', '🌿'], // 夏：绿叶、新芽
    2: ['🌰', '🍂'],       // 秋：红枫、落叶
    3: ['❄️']              // 冬：雪花
  };

  document.addEventListener('mousemove', (e) => {
    const now = Date.now();
    // 节流阀：控制粒子生成频率。每 200 毫秒最多生成一个，避免生成过多导致浏览器卡顿
    if (now - lastParticleTime < 200) return;
    lastParticleTime = now;

    // 1. 获取当前主题 (通过判断 body 上挂载的 class 最准确)
    let themeIdx = 0; 
    if (document.body.classList.contains('theme-summer')) themeIdx = 1;
    else if (document.body.classList.contains('theme-autumn')) themeIdx = 2;
    else if (document.body.classList.contains('theme-winter')) themeIdx = 3;

    // 2. 创建粒子 DOM 元素
    const particle = document.createElement('div');
    particle.className = 'mouse-particle';
    
    // 3. 随机获取当前主题下的一片“叶子”或“雪花”
    const icons = themeParticles[themeIdx];
    particle.innerText = icons[Math.floor(Math.random() * icons.length)];
    
    // 4. 设置初始位置，使其紧跟鼠标
    particle.style.left = e.clientX + 'px';
    particle.style.top = e.clientY + 'px';
    
    // 5. 随机生成 CSS 动画的终点偏移量和旋转角度 (通过 CSS 变量传给上面的 keyframes)
    const tx = (Math.random() - 0.5) * 120 + 'px'; // 左右随机散开 (-60px 到 60px)
    const ty = (Math.random() * 80 + 40) + 'px';   // 向下掉落的距离 (40px 到 120px)
    const rot = (Math.random() - 0.5) * 360 + 'deg'; // 随机翻滚旋转
    
    particle.style.setProperty('--tx', tx);
    particle.style.setProperty('--ty', ty);
    particle.style.setProperty('--rot', rot);
    
    // 6. 随机改变一点大小，让画面更有景深和层次感
    particle.style.fontSize = (Math.random() * 10 + 14) + 'px'; 

    // 7. 把粒子加入到页面中
    document.body.appendChild(particle);

    // 8. 动画结束后（1秒），必须把 DOM 节点删掉，否则页面里元素会无限增多，导致崩溃
    setTimeout(() => {
      particle.remove();
    }, 1000); 
  });
})();

// === 移动端导航栏展开/折叠逻辑 ===
  window.toggleMobileMenu = function() {
    const navLinks = document.querySelector('.navbar-links');
    if (navLinks) {
      navLinks.classList.toggle('open');
    }
  };

  // 点击页面其他空白区域时，自动收起移动端菜单
  document.addEventListener('click', function(event) {
    const navLinks = document.querySelector('.navbar-links');
    const mobileBtn = document.querySelector('.navbar-mobile-btn');
    
    // 如果菜单是打开的，并且点击的位置不在菜单和按钮内部
    if (navLinks && navLinks.classList.contains('open') && !navLinks.contains(event.target) && !mobileBtn.contains(event.target)) {
      navLinks.classList.remove('open');
    }
  });
